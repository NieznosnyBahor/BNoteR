    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int id = size;
        String dateD = tfDateDay.getText();
        String dateM = tfDateMonth.getText();
        String dateY = tfDateYear.getText();
        String date = dateD + ":" + dateM + ":" + dateY;
        double distance = Integer.parseInt(tfDistance.getText());
        double timeHours = Double.parseDouble(tfTimeHours.getText());
        double timeMins = Double.parseDouble(tfTimeMins.getText());
        double vMax = Double.parseDouble(tfVMax.getText());
        
        
        Rekord r = new Rekord( id, date, distance, timeHours, timeMins, vMax );
        database.addRecord(r);
        dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

